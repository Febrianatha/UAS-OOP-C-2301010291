    private void cBARUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cBARUActionPerformed
        String ID_TRANSAKSI = txID.getText();
        String TANGGAL = txTGL.getText();
        String JENIS_TRANSAKSI = txTRANSAKSI.getSelectedItem().toString();
        String NOMINAL = txNOMINAL.getText();
        String KETERANGAN = txKET.getText();

    // Validasi sederhana agar data tidak kosong
    if(ID_TRANSAKSI.isEmpty() || TANGGAL.isEmpty() || JENIS_TRANSAKSI.isEmpty() || NOMINAL.isEmpty()){
        JOptionPane.showMessageDialog(this, "Mohon isi semua data (ID, Tanggal, Jenis, Nominal)!");
        return;
    }
     try {
        Connection c = koneksi();
        String sql = "INSERT INTO transaksi VALUES (?, ?, ?, ?, ?)";
        PreparedStatement p = c.prepareStatement(sql);
        p.setString(1, ID_TRANSAKSI);
        p.setString(2, TANGGAL);
        p.setString(3, JENIS_TRANSAKSI);
        p.setDouble(4, Double.parseDouble(NOMINAL));
        p.setString(5, KETERANGAN);
        p.executeUpdate();
        p.close();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal simpan: " + e.getMessage());
    }
    
    // refresh tabel
    loadDataFromDatabase();

  
    // Clear input setelah tambah data
    txID.setText("");
    txTGL.setText("");
    txTRANSAKSI.setSelectedIndex(0);
    txNOMINAL.setText("");
    txKET.setText("");

    // Fokus ke ID lagi agar user bisa input data berikutnya
    txID.requestFocus();
    
    }//GEN-LAST:event_cBARUActionPerformed

    private void cHAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cHAPUSActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbltransaksi.getSelectedRow();
        if(selectedRow >= 0){
            int confirm = JOptionPane.showConfirmDialog(this,
                    "Apakah Anda yakin ingin menghapus data ini?",
                    "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION);
            if(confirm == JOptionPane.YES_OPTION){
            String id = tbltransaksi.getValueAt(selectedRow, 0).toString();
            try {
                Connection c = koneksi();
                String sql = "DELETE FROM transaksi WHERE ID_TRANSAKSI=?";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, id);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal hapus: " + e.getMessage());
            }

            loadDataFromDatabase();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
    }

    }//GEN-LAST:event_cHAPUSActionPerformed

