    private void cUBAHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cUBAHActionPerformed
        // TODO add your handling code here:
     int selectedRow = tbltransaksi.getSelectedRow();
    if(selectedRow >= 0){
        String ID_TRANSAKSI = txID.getText();
        String TANGGAL = txTGL.getText();
        
        String NOMINAL = txNOMINAL.getText();
        String KETERANGAN = txKET.getText();

        try {
            Connection c = koneksi();
            String sql = "UPDATE transaksi SET TANGGAL=?, JENIS_TRANSAKSI=?, NOMINAL=?, KETERANGAN=? WHERE ID_TRANSAKSI=?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, TANGGAL);
            String JENIS_TRANSAKSI = txTRANSAKSI.getSelectedItem().toString();
            p.setString(2, JENIS_TRANSAKSI);
            p.setDouble(3, Double.parseDouble(NOMINAL));
            p.setString(4, KETERANGAN);
            p.setString(5, ID_TRANSAKSI);
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal update: " + e.getMessage());
        }

        loadDataFromDatabase();

        // clear
        txID.setText("");
        txTGL.setText("");
        txTRANSAKSI.setSelectedIndex(0);
        txNOMINAL.setText("");
        txKET.setText("");
    } else {
        JOptionPane.showMessageDialog(this, "Pilih dulu data yang mau diubah!");
        }
    }//GEN-LAST:event_cUBAHActionPerformed

